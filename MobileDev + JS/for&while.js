// for loop counting
for (var i = 1; i <= 5; i++){
    console.log(i)
}
console.log('Counting completed!')

for (var i = 5; i >= 1; i--){
    console.log(i)
}
console.log('Countdown finished!')

// while loop counting
var count = 1
while (count <= 5){
    console.log(count)
    count++
}
console.log('Counting completed!')

var count = 5
while (count >= 1){
    console.log(count)
    count = count - 1
}
console.log('Countdown finished!')

var year = 2018
while (year <= 2022){
    console.log(year)
    year++
}

